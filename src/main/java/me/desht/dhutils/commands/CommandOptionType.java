package me.desht.dhutils.commands;

enum CommandOptionType {

    /**
     * This represents a {@link Boolean} option.
     */
    BOOLEAN,

    /**
     * This represents a {@link String} option.
     */
    STRING,

    /**
     * This represents an {@link Integer} option.
     */
    INT,

    /**
     * This represents a {@link Double} option.
     */
    DOUBLE;
}